// Language list is shared with the voice layer (gemini.LANGUAGES) so codes match.
import { LANGUAGES } from './gemini'
export { LANGUAGES }

const LANG_KEY = 'san4_lang'

export function getLang() {
  try { return localStorage.getItem(LANG_KEY) || 'en-US' } catch { return 'en-US' }
}
export function hasChosenLang() {
  try { return !!localStorage.getItem(LANG_KEY) } catch { return false }
}
export function setLang(code) {
  try { localStorage.setItem(LANG_KEY, code) } catch { /* ignore */ }
}

